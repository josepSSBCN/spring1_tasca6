package ClassPackage;

public class SafeBox {
    //region ATTRIBUTES
    private int heigh;
    private int wide;
    private int deep;

    //endregion ATTRIBUTES


    //region CONSTRUCTOR
    public SafeBox(int heigh, int wide, int deep) {
        this.heigh = heigh;
        this.wide = wide;
        this.deep = deep;
    }

    //endregion CONSTRUCTOR


    //region GETTERS & SETTERS

    public int getHeigh() {
        return heigh;
    }

    public void setHeigh(int heigh) {
        this.heigh = heigh;
    }

    public int getWide() {
        return wide;
    }

    public void setWide(int wide) {
        this.wide = wide;
    }

    public int getDeep() {
        return deep;
    }

    public void setDeep(int deep) {
        this.deep = deep;
    }

    //endregion GETTERS & SETTERS


    //region METHODS

    //endregion METHODS


}
